#Indicate the type of the expression.
#Choose between: int, float, bool, NoneType,String, List, Tuple, Dict, Function


print(type(2+3)) #int
print(type(6/2)) #float
print(type(2 != 3)) #bool
print(type(5 or 6)) #int
print(type(5 & 7)) #int
print(type(print)) #built-in function or method
print(type(print(2*2))) #NoneType
print(type("abc".find)) #built-in function or method
print(type("bubu"*2)) #String
# print(type("bubu"*(4/2))) #TypeError: can't multiply sequence by non-int of type 'float'
print(type(["abc", 2])) #list
print(type((2))) #int



